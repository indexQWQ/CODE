#include <stdio.h>

int main() 
{
    int a = 5, b = 3;
    int c = a & b; // 按位与
    //000000000000000000000101
    //000000000000000000000011
    //000000000000000000000001
    int d = a | b; // 按位或
    //000000000000000000000101
    //000000000000000000000011
    //000000000000000000000111
    int e = a ^ b; // 按位异或//不同为1，相同为0
    //000000000000000000000101
    //000000000000000000000011
    //000000000000000000000110
    printf("a & b = %d\n", c);
    printf("a | b = %d\n", d);
    printf("a ^ b = %d\n", e);
    return 0;
}