// 实现不创建临时变量交换值
// 但是实际生产工作中还是以创建变量为主
// 而且这种交换只适用于整数
#include <stdio.h>

int main()
{
    int a = 3, b = 5;
    printf("before a=%d b=%d\n",a,b);
    // a=a+b;//但是会溢出
    // b=a-b;
    // a=a-b;
    a=a^b;//3^5 -->a=3^5
    b=a^b;//3^5^5 -->b=3
    a=a^b;//3^5^3 -->a=5
    printf("after a=%d b=%d\n",a,b);
    return 0;
}
